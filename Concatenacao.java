public class Concatenacao {

    public static void main(String[] args) {

        // Variaveis
        String nome = "João";
        String sobrenome = "Silva";
        int idade = 25;

        // Concatenacao
        String frase = "Meu nome é " + nome + " " + sobrenome +
                " e eu tenho " + idade + " anos.";
        System.out.println(frase);
        // Fim do programa

    }
}