public class Condicionais {
    
    public static void main(String[] args) {
        
        //Variavel 

        double nota = 4;

        //Condicional

        if(nota >= 7){
            System.out.println("Aprovado");
        }else if(nota >=5){
            System.out.println("Recuperacao");
        }else {
            System.out.println("Reprovado");

        }
    }
}
