public class OperadoresRelacionais {
    
    public static void main(String[] args) {
        
        //Variáveis

        int a = 10;
        int b = 3;

        // Operadores Relacionais
        System.out.println("a é igual a b: " + (a == b)); // Igualdade
        System.out.println("a é diferente de b: " + (a != b)); // Diferença
        System.out.println("a é maior que b: " + (a > b)); // Maior que
        System.out.println("a é menor que b: " + (a < b)); // Menor que
        System.out.println("a é maior ou igual a b: " + (a >= b)); // Maior ou igual
        System.out.println("a é menor ou igual a b: " + (a <= b)); // Menor ou igual
        System.out.println("Fim dos operadores relacionais.");
    }
}
